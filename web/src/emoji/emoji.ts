// Flattened view, built once at module load. Used by search and by any
// caller that wants "all emoji" without caring about grouping.
export const ALL_EMOJI: Emoji[] = EMOJI_CATEGORIES.flatMap((c) => c.emoji);

// searchEmoji matches the query against the name and the keywords. Substring
// rather than prefix so "smile" finds "sweat smile", and results are ranked
// so exact and prefix name matches float above incidental keyword hits --
// otherwise typing "cat" surfaces every animal before the cat itself.
export function searchEmoji(query: string, limit = 120): Emoji[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  const scored: { e: Emoji; score: number }[] = [];
  for (const e of ALL_EMOJI) {
    let score = -1;
    if (e.n === q) score = 0;
    else if (e.n.startsWith(q)) score = 1;
    else if (e.k.some((k) => k === q)) score = 2;
    else if (e.n.includes(q)) score = 3;
    else if (e.k.some((k) => k.startsWith(q))) score = 4;
    else if (e.k.some((k) => k.includes(q))) score = 5;
    if (score >= 0) scored.push({ e, score });
  }
  scored.sort((a, b) => a.score - b.score);
  return scored.slice(0, limit).map((s) => s.e);
}

// insertAtCursor splices `insert` into `value` at the given selection range,
// returning the new value and where the caret should land. Pure so the
// caret arithmetic is testable without a DOM -- getting this wrong is the
// classic emoji-picker bug (text appended at the end, caret jumping to 0).
export function insertAtCursor(
  value: string,
  insert: string,
  selectionStart: number,
  selectionEnd: number,
): { value: string; caret: number } {
  const len = value.length;
  // Clamp: a stale selection (element re-rendered since) must not throw or
  // silently truncate the draft.
  const start = Math.max(0, Math.min(selectionStart, len));
  const end = Math.max(start, Math.min(selectionEnd, len));
  const next = value.slice(0, start) + insert + value.slice(end);
  return { value: next, caret: start + insert.length };
}
