//
// Hand-curated rather than pulled from a full Unicode dataset: the complete
// set is ~1,900 emoji and several hundred KB of names/keywords, which would
// dominate the bundle for a chat app where a few hundred cover essentially
// all real use. Everything here is widely supported across platforms, so we
// avoid the tofu boxes that newer additions still produce on older systems.
//
// Skin-tone variants are deliberately omitted -- they multiply the list by
// six for a feature nobody has asked for yet. Adding them later means a
// modifier row in the picker, not a change to this shape.
//
